public class Loops {

    public static void main (String[] args) {

        //------Ciclo For
        int num = 0;
        for (int i = 0; i < 8; i++) {
            num++;
            //System.out.println(num);
        }

        //------Ciclo For Each
        int nums[]={1,2,3,4,5};
        int sum=0;
        for (int x: nums) System.out.println(sum+=x);
    }
}
